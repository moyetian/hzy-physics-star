function makeQuizGroup(topic, rows) {
  return rows.map(([concept, question, correct, wrongOne, wrongTwo, explain]) => ({
    topic,
    concept,
    question,
    correct,
    wrong: [wrongOne, wrongTwo],
    explain
  }));
}

function makeFormulaGroup(topic, rows) {
  return rows.map(([concept, answer, wrongOne, wrongTwo, explain]) => ({
    topic,
    concept,
    answer,
    wrong: [wrongOne, wrongTwo],
    explain
  }));
}

function buildQuizQuestions(points) {
  return points.flatMap((point) => [
    {
      topic: point.topic,
      question: `【${point.topic}】${point.question}`,
      options: [point.correct, ...point.wrong],
      answer: 0,
      explain: point.explain
    },
    {
      topic: point.topic,
      question: `【${point.topic}】关于“${point.concept}”，哪一句最准确？`,
      options: [point.correct, ...point.wrong],
      answer: 0,
      explain: point.explain
    }
  ]);
}

function buildMatchCards(points) {
  return points.flatMap((point, index) => {
    const conceptOptions = points
      .filter((candidate) => candidate.concept !== point.concept)
      .slice(index + 1)
      .concat(points)
      .filter((candidate) => candidate.concept !== point.concept)
      .slice(0, 2)
      .map((candidate) => `对应：${candidate.concept}`);

    return [
      {
        topic: point.topic,
        prompt: `【${point.topic}】${point.concept}`,
        options: [point.answer, ...point.wrong],
        answer: 0,
        explain: point.explain
      },
      {
        topic: point.topic,
        prompt: point.answer,
        options: [`对应：${point.concept}`, ...conceptOptions],
        answer: 0,
        explain: point.explain
      }
    ];
  });
}

